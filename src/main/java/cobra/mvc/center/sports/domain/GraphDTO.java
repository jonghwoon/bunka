package cobra.mvc.center.sports.domain;

public class GraphDTO {
	
	String id;
	float str;
	float agi;
	float mag;
	float hp;
	float mp;
	float def;
	float total;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public float getStr() {
		return str;
	}
	public void setStr(float str) {
		this.str = str;
	}
	public float getAgi() {
		return agi;
	}
	public void setAgi(float agi) {
		this.agi = agi;
	}
	public float getMag() {
		return mag;
	}
	public void setMag(float mag) {
		this.mag = mag;
	}
	public float getHp() {
		return hp;
	}
	public void setHp(float hp) {
		this.hp = hp;
	}
	public float getMp() {
		return mp;
	}
	public void setMp(float mp) {
		this.mp = mp;
	}
	public float getDef() {
		return def;
	}
	public void setDef(float def) {
		this.def = def;
	}
	public float getTotal() {
		return total;
	}
	public void setTotal(float total) {
		this.total = total;
	}

}
